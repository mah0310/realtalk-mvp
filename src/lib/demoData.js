// Get demo answers based on question text
export function getDemoAnswers(questionText) {
  if (!questionText) return [];
  
  // Find matching keyword
  for (const [keyword, answers] of Object.entries(DEMO_ANSWERS)) {
    if (questionText.includes(keyword)) {
      return answers.map((text, index) => ({
        id: `demo-${keyword}-${index}`,
        answer_text: text,
        is_public: true,
        is_demo: true,
        created_at: getRandomPastTime(),
        profiles: {
          id: `demo-user-${index}`,
          username: DEMO_USERS[index].name,
          display_name: DEMO_USERS[index].name,
          icon_char: DEMO_USERS[index].char,
          icon_color: DEMO_USERS[index].color,
          group_id: null,
        },
        reactions: generateRandomReactions(index),
      }));
    }
  }
  
  return [];
}

// Generate random past time (1-24 hours ago)
function getRandomPastTime() {
  const now = new Date();
  const hoursAgo = Math.floor(Math.random() * 24) + 1;
  return new Date(now - hoursAgo * 60 * 60 * 1000).toISOString();
}

// Generate random reactions
function generateRandomReactions(seed) {
  const sameCount = Math.floor(Math.random() * 15) + 1;
  const niceCount = Math.floor(Math.random() * 10) + 1;
  
  const reactions = [];
  for (let i = 0; i < sameCount; i++) {
    reactions.push({ id: `demo-r-same-${seed}-${i}`, reaction_type: 'same', user_id: `demo-u-${i}` });
  }
  for (let i = 0; i < niceCount; i++) {
    reactions.push({ id: `demo-r-nice-${seed}-${i}`, reaction_type: 'nice', user_id: `demo-u-${i + 20}` });
  }
  
  return reactions;
}
